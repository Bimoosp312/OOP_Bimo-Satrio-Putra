        // TODO add your handling code here:
        
        MotorMatic ms = new MotorMatic();
        String Nama,Merk,Warna,Plat;
        memo2.setText("");

        Nama = txtNama.getText();
        Merk = txtMerk.getText();
        Warna = txtWarna.getText();
        Plat = txtPlat.getText();
        

        ms.harga = Integer.parseInt(txtHarga.getText());
        ms.CicilanPerbulan = Integer.parseInt(txtCicilan.getText());
