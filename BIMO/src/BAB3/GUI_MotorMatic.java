        memo2.append(" ======================= "+ "\n");

        memo2.append("Nama  : " + Nama + "\n");
        memo2.append("Merk  : " + Merk + "\n");
        memo2.append("Warna   : " + Warna + "\n");
        memo2.append("Plat Nomor   : " + Plat + "\n");
        memo2.append("Harga   : Rp " + ms.harga + "\n");
        memo2.append("Cicilan Perbulan  : " + ms.CicilanPerbulan + " bulan" +"\n");
   
