        // TODO add your handling code here:
        
        MotorSport ms = new MotorSport();
        String Nama, Alamat, Merk;
        memo1.setText("");

        Nama = txtNama.getText();
        Alamat = txtAlamat.getText();
        Merk = txtMerk.getText();

        ms.harga = Integer.parseInt(txtHarga.getText());
        ms.durasiCicilan = Integer.parseInt(txtDurasi.getText());
