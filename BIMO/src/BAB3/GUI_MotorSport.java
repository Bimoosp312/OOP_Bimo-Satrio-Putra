        memo1.append(" ======================= "+ "\n");

        memo1.append("Nama  : " + Nama + "\n");
        memo1.append("Alamat   : " + Alamat + "\n");
        memo1.append("Merk  : " + Merk + "\n");
        memo1.append("Harga   : " + ms.harga + "\n");
        memo1.append("Durasi Cicilan  : " + ms.durasiCicilan + " bulan" +"\n");
       
